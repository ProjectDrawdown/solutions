        self.ref1_low_edu = pd.DataFrame(ref1_low_edu_list[1:],
            columns=ref1_low_edu_list[0],
            index=list(range(2014, 2061)), dtype=np.float64)

        # Population_Tables!O61:X108
        self.ref2_low_edu = pd.DataFrame(ref2_low_edu_list[1:],
            columns=ref2_low_edu_list[0],
            index=list(range(2014, 2061)), dtype=np.float64)

